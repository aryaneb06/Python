class Employee:
    language="Py" # these three are class attributes
    age ="23"
    salary="1000000"

aryan=Employee() #object define -- object is basically an class instance which holds/allocates the memory
aryan.name = "Aryan"   #these are object attributes
print(aryan.name,aryan.age,aryan.salary,aryan.language)

rohan=Employee() #object define -- object is basically an class instance which holds/allocates the memory
rohan.name="Rohan"
print(rohan.name,rohan.age,rohan.salary,rohan.language)

